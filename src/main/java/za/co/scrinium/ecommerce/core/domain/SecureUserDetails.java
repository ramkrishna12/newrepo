package za.co.scrinium.ecommerce.core.domain;

import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import java.util.ArrayList;
import java.util.Collection;


/**
 * Created By Leo Ackerman
 * Created On 2014/01/20 2:08 PM
 */

public class SecureUserDetails implements UserDetails {

  private String username;
  private String password;
  private Collection<GrantedAuthority> authorities;

  public SecureUserDetails(String aUsername, String aPassword) {
    username = aUsername;
    password = aPassword;
    authorities = new ArrayList<>();
    authorities.add(new SimpleGrantedAuthority("USER"));
  }

  @Override
  public Collection<? extends GrantedAuthority> getAuthorities() {
    return authorities;
  }

  @Override
  public String getPassword() {
    return password;
  }

  @Override
  public String getUsername() {
    return username;
  }

  @Override
  public boolean isAccountNonExpired() {
    return true;
  }

  @Override
  public boolean isAccountNonLocked() {
    return true;
  }

  @Override
  public boolean isCredentialsNonExpired() {
    return true;
  }

  @Override
  public boolean isEnabled() {
    return true;
  }
}
